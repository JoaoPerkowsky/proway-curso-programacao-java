        var tipo = jComboBoxTipo.getSelectedItem().toString();
        
        var id = jogoServico.adicionar(nome, tipo);
        
        if(id != -1){
            JOptionPane.showMessageDialog(this, "Jogo cadastrado com sucesso");
            dispose();
        }
